const scrabble = require('../src/scrabble')

console.log(' ')
console.log('                 Scrabble')
console.log('——————————————————————————————————————————')
console.log('            Letter Multipliers')
console.log('——————————————————————————————————————————')
console.log('returns 6 for double letter o')
console.log(scrabble('d{o}g'))
console.log('returns 7 for triple letter o')
console.log(scrabble('d[o]g'))
console.log('returns 0 for invalid multiplier')
console.log(scrabble('d{og'))
console.log('returns 0 for invalid multiplier')
console.log(scrabble('do}g'))
console.log('——————————————————————————————————————————')
console.log('             Word Multipliers')
console.log('——————————————————————————————————————————')
console.log('returns 10 for double letter o')
console.log(scrabble('{dog}'))
console.log('returns 15 for triple letter o')
console.log(scrabble('[dog]'))
console.log('returns 0 for invalid multiplier')
console.log(scrabble('{dog'))
console.log('returns 0 for invalid multiplier')
console.log(scrabble('dog}'))
console.log('——————————————————————————————————————————')
console.log('                Edge Cases')
console.log('——————————————————————————————————————————')
console.log('returns 18 for letter and word multiplier')
console.log(scrabble('{[d]og}'))
console.log('returns 30 for two word multipliers')
console.log(scrabble('[{dog}]'))
console.log('returns 9 for two end letter multipliers')
console.log(scrabble('{d}o{g}'))
console.log('returns 0 for incorrect tokens')
console.log(scrabble('|d|og'))
console.log('——————————————————————————————————————————')
